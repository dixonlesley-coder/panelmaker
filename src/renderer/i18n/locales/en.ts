/**
 * Recursively widen the leaf literal types of the `en` resource to `string`,
 * so other locales (e.g. `id`) can implement the SAME key set with their OWN
 * translated values. `typeof en` alone would pin every leaf to its English
 * literal, which a translation can never satisfy.
 */
type LocaleShape<T> = {
  [K in keyof T]: T[K] extends string ? string : LocaleShape<T[K]>;
};

/** The structural contract every locale must satisfy (key set of `en`). */
export type TranslationKeys = LocaleShape<typeof en>;
